
package app.clase4.practica.granjaganadera;

public abstract class Alimento {
    
         private String nombreDieta;
         private String tipoAlimento;
         private String unidadMedid;
         private String unidaddMedida;
         private String valorNutirnte;
         private String nutirnteContenido;
         private String cantidadNutirnte;

    public String getNombreDieta() {
        return nombreDieta;
    }

    public void setNombreDieta(String nombreDieta) {
        this.nombreDieta = nombreDieta;
    }

    public String getTipoAlimento() {
        return tipoAlimento;
    }

    public void setTipoAlimento(String tipoAlimento) {
        this.tipoAlimento = tipoAlimento;
    }

    public String getUnidadMedid() {
        return unidadMedid;
    }

    public void setUnidadMedid(String unidadMedid) {
        this.unidadMedid = unidadMedid;
    }

    public String getUnidaddMedida() {
        return unidaddMedida;
    }

    public void setUnidaddMedida(String unidaddMedida) {
        this.unidaddMedida = unidaddMedida;
    }

    public String getValorNutirnte() {
        return valorNutirnte;
    }

    public void setValorNutirnte(String valorNutirnte) {
        this.valorNutirnte = valorNutirnte;
    }

    public String getNutirnteContenido() {
        return nutirnteContenido;
    }

    public void setNutirnteContenido(String nutirnteContenido) {
        this.nutirnteContenido = nutirnteContenido;
    }

    public String getCantidadNutirnte() {
        return cantidadNutirnte;
    }

    public void setCantidadNutirnte(String cantidadNutirnte) {
        this.cantidadNutirnte = cantidadNutirnte;
    }

    
    
    
}
