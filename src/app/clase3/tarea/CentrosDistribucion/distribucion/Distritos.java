
package app.clase3.tarea.CentrosDistribucion.distribucion;

public abstract class Distritos {
    
     private String Lima;
     private String Miraflores;
     private String SanIsidro;
     private String Breña;
     private String Callao;
     private String LaPunta;
     private String LaMolina;
     private String SanMiguel;

    public String getLima() {
        return Lima;
    }

    public void setLima(String Lima) {
        this.Lima = Lima;
    }

    public String getMiraflores() {
        return Miraflores;
    }

    public void setMiraflores(String Miraflores) {
        this.Miraflores = Miraflores;
    }

    public String getSanIsidro() {
        return SanIsidro;
    }

    public void setSanIsidro(String SanIsidro) {
        this.SanIsidro = SanIsidro;
    }

    public String getBreña() {
        return Breña;
    }

    public void setBreña(String Breña) {
        this.Breña = Breña;
    }

    public String getCallao() {
        return Callao;
    }

    public void setCallao(String Callao) {
        this.Callao = Callao;
    }

    public String getLaPunta() {
        return LaPunta;
    }

    public void setLaPunta(String LaPunta) {
        this.LaPunta = LaPunta;
    }

    public String getLaMolina() {
        return LaMolina;
    }

    public void setLaMolina(String LaMolina) {
        this.LaMolina = LaMolina;
    }

    public String getSanMiguel() {
        return SanMiguel;
    }

    public void setSanMiguel(String SanMiguel) {
        this.SanMiguel = SanMiguel;
    }
    
     
}
